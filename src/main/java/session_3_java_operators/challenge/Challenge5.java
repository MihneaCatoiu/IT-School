package session_3_java_operators.challenge;

public class Challenge5 {

    public static void main(String[] args) {

        int a = 6;
        a += 5;
        System.out.println("a += " + a);

        a -= 2;
        System.out.println("a -= " + a);

        a *= 7;
        System.out.println("a *= " + a);

        a /= 9;
        System.out.println("a /= " + a);

        a %= 1;
        System.out.println("a %= " + a);


    }
}
