package session_3_java_operators.challenge;

public class Challenge11 {

    public static void main(String[] args) {

        boolean areDinosaursDead = true;
        boolean areDinosaursAlive = false;

        System.out.println("Question: are dinosaurs alive? " + areDinosaursAlive);
        System.out.println("Question: are dinosaurs alive? (negative)" + !areDinosaursAlive);
        System.out.println("Question: are dinosaurs dead? " + areDinosaursDead);
        System.out.println("Question: are dinosaurs dead? (negative) " + !areDinosaursDead);
    }
}
