package session_3_java_operators.challenge;

public class Challenge7 {

    public static void main(String[] args) {

        int firstNumber = 5;
        double secondNumber = 71;

        System.out.println("First number + second number = " + (firstNumber + secondNumber));
        System.out.println("First number - second number = " + (firstNumber - secondNumber));
        System.out.println("First number * second number = " + (firstNumber * secondNumber));
        System.out.println("First number / second number = " + (firstNumber / secondNumber));
        System.out.println("First number % second number = " + (firstNumber % secondNumber));
    }
}
