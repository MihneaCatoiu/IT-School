public class Challenge15 {
    public static void main(String[] args) {
        int total = 100;

        total -= 20;
        System.out.println("After decreasing by 20, total is: " + total);

        total *= 2;
        System.out.println("After multiplying by 2, total is: " + total);
    }
}
